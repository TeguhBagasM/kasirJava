    public void read(){
        
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try {
            
            Connection conn = DB.getKoneksi();
            Statement stm = conn.createStatement();
            String sql = "SELECT idMenu, tipeMenu, namaMenu, hargaa FROM daftarmenu";
            ResultSet rs = stm.executeQuery(sql);
            
            while(rs.next()) {
                
                Object[] data = new Object[4];
                data[0] = rs.getString("idMenu");
                data[1] = rs.getString("tipeMenu");
                data[2] = rs.getString("namaMenu");
                data[3] = rs.getString("hargaa");
                
                model.addRow(data);
                
            }
            
            rs.close();
            stm.close();
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(this, e.getMessage());
            
        }
        
    }
    
