    private void ubahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ubahMouseClicked
        
        int t = tabelMenu.getSelectedRow();
        
        if (t == -1) {
            return;
        }
        
        String id_menu = (String) model.getValueAt(t, 0);
        String tipemenu = (String) menutipe.getSelectedItem();
        String nama_menu = nama_Menu.getText();
        String hargaaa = harga.getText();
        
        if(tipemenu == "Pilih Tipe Menu") {
            
            JOptionPane.showMessageDialog(null, "Pilih Tipe Menu Yang Benar!");
            
        } else if (nama_Menu.getText().equals("") || harga.getText().equals("")) {
            
            JOptionPane.showMessageDialog(null, "Field Tidak Boleh Kosong!");
            
        } else if (nama_Menu.getText().matches("[0-9]*")) {
            
            JOptionPane.showMessageDialog(null, "Field Nama Menu Harus String!");
            
        } else if (!harga.getText().matches("[0-9]*")) {
            
            JOptionPane.showMessageDialog(null, "Field Harga Harus Angka!");
            
        } else {
        
            int pernyataan = JOptionPane.showConfirmDialog(null, "Yakin Mengubah Data?","Konfirmasi", 
                    JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (pernyataan == JOptionPane.OK_OPTION) {

                try {

                    Connection conn = DB.getKoneksi();
                    String sql = "UPDATE daftarmenu SET namaMenu='" + nama_menu +
                            "', tipeMenu='" + tipemenu + "', hargaa='" + hargaaa + "' WHERE idMenu='" + id_menu + "'";
                    Statement pr = conn.createStatement();
                    pr.executeUpdate(sql);

                    pr.close();
                    JOptionPane.showMessageDialog(null, "Menu Berhasil Diubah");

                    simpan.setEnabled(true);
                    ubah.setEnabled(false);
                    hapus.setEnabled(false);
                    batal.setEnabled(false);
                    clear();

                } catch(Exception e) {

                    JOptionPane.showMessageDialog(this, e.getMessage());

                } finally {

                    autonumber();
                    read();

                }

            } if (pernyataan == JOptionPane.CANCEL_OPTION) {

            }
         
        }
        
    }//GEN-LAST:event_ubahMouseClicked

