    private void hapusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_hapusMouseClicked
        
        int t = tabelMenu.getSelectedRow();
        
        if (t == -1) {
            return;
        }
        
        String id_menu = (String) model.getValueAt(t, 0);
        
        int pernyataan = JOptionPane.showConfirmDialog(null, "Yakin Menghapus Data?","Konfirmasi", 
                JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if (pernyataan== JOptionPane.OK_OPTION) {
            
            try {
                
                Connection conn = DB.getKoneksi();
                String sql = "DELETE FROM daftarmenu WHERE idMenu ='" + id_menu + "'";
                Statement pr = conn.createStatement();
                pr.executeUpdate(sql);
                pr.close();
                
                JOptionPane.showMessageDialog(null, "Data Telah Terhapus");
                
            } catch (Exception e) {
                
                JOptionPane.showMessageDialog(null, e.getMessage());
                
            }finally{
                
                simpan.setEnabled(true);
                ubah.setEnabled(false);
                hapus.setEnabled(false);
                batal.setEnabled(false);
                read();
                autonumber();
                clear();
                
            }
            
        }
        
        if (pernyataan== JOptionPane.CANCEL_OPTION) {
            
        }
        
    }//GEN-LAST:event_hapusMouseClicked

